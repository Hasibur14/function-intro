// condition using odd number in function..............
function oddNumber(number) {
    if (number % 2 === 1) {
        return true;
    }
    else {
        return false;
    }

}
console.log(oddNumber(5621));


// condition using Even number in function............

function evenNumber(number2) {
    if (number2 % 2 === 0) {
        return true;
    } 
    else {
        return false;
    }
}
